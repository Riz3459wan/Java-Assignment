// Demonstrates the difference between let, const, and var
// ========================================================

// let
// ========
// It is a keyword used to declare a variable.
// It has block scope.
// We can reassign the value, but we cannot redeclare it in the same scope.

// var
// ========
// It is a keyword used to declare a variable.
// It has function scope (or global scope if declared outside a function).
// We can reassign and redeclare the same variable.

// const
// ========
// It is a keyword used to declare a variable.
// It has block scope.
// We cannot reassign or redeclare the variable.
